        String[] forecastArray = {
                "Today - Sunny 34c",
                "Tomorrow - dark - 44c",
                "Sunday - black - 44f",
        };

        //String URL_TO_WEATHER = "http://api.openweathermap.org/data/2.5/forecast/daily?q=95136&mode=json&units=metric&cnt=7";

